# ── 负面情绪关键词列表 ──
# 语义: 用户对服务/产品不满的一般性负面表达
# 特征: 表达不满, 但尚未到"准备采取实际行动"的程度
# 触发级别: 累计计数 negative_count, 用于情绪强度计算
# 覆盖面: 电商场景中最常见的抱怨用词
EMOTION_NEGATIVE_WORDS = ["差", "烂", "垃圾", "废物", "骗", "坑", "恶心", "讨厌", "生气"]

# ── 愤怒情绪关键词列表 ──
# 语义: 用户已经非常愤怒, 可能准备采取实际行动 (投诉/曝光/报警等)
# 特征: 表达强烈不满, 且通常伴随具体的威胁或行动意图
# 触发级别: 累计计数 angry_count, 权重高于 negative_count
# 特别注意: 前4个词 ["投诉", "消协", "曝光", "12315"] 在升级决策的加权评分中有额外加权
#   (见 detect_escalation 方法中的 EMOTION_ANGRY_WORDS[:4])
EMOTION_ANGRY_WORDS = ["投诉", "消协", "曝光", "12315", "赔偿", "法院", "报警", "骂"]


def detect_emotion(text: str) -> tuple[str, float]:
    """情绪检测: 基于关键词计数的用户情绪识别

    通过统计文本中负面/愤怒关键词的出现次数, 判断用户当前的情绪状态和强度。
    结果用于:
      1) 升级决策引擎 (detect_escalation) - 作为转人工的关键信号
      2) 回复语气调节 - 负面情绪时增加共情/安抚表达
      3) 客服工作台展示 - 情绪曲线 sparkline (参考 §4.3 客服工作台)
      4) 质检分析 - 评估AI安抚效果 (参考 §3.7 质检)

    算法逻辑 (多级阈值判定, 优先级从高到低):
    ─────────────────────────────────────────────────────────────────

    判定1 - angry (愤怒):
       条件: angry_count >= 2, 或 (angry_count >= 1 且 negative_count >= 1)
       含义: 多个愤怒词同时出现, 或愤怒+负面词组合, 表明强烈不满
       分数公式: 0.5 + angry×0.2 + negative×0.1, 上限 1.0
       示例: "你们是骗子! 我要投诉到消协!"
             → angry=2("投诉","消协"), negative=1("骗")
             → score = 0.5 + 2×0.2 + 1×0.1 = 1.0 (截断)

    判定2 - negative (明显负面):
       条件: negative_count >= 2, 或 angry_count == 1
       含义: 明显不满但未到愤怒程度
       分数公式: 0.4 + negative×0.15, 上限 0.85
       示例: "你们服务太差了, 东西也是垃圾"
             → negative=2("差","垃圾")
             → score = 0.4 + 2×0.15 = 0.7
       为何单个angry归为negative而非angry:
             → 单独的"投诉"可能只是口头威胁, 尚未极端愤怒

    判定3 - negative (轻度负面):
       条件: negative_count == 1
       含义: 有轻微不满情绪, 但不严重
       分数公式: 0.3 + negative×0.1 = 0.4
       示例: "这东西有点差劲" → negative=1 → score=0.4

    判定4 - neutral (中性):
       条件: 以上均不满足 (无负面/愤怒关键词命中)
       分数: 固定 0.1
       为何是 0.1 而非 0?
             → 区分"已检测为中性"和"未进行检测", 便于下游逻辑判断
       示例: "帮我查一下物流" → score=0.1

    设计取舍与局限:
    ─────────────────────────────────────────────────────────────────
    · 优点: 快速、无依赖、可解释
    · 局限: 无法识别语气/讽刺 ("真是太好了呢" ← 反讽)
    · 局限: 无法处理否定 ("不生气" 也会命中"生气")
    · 这些问题在 BERT-emotion 模型中可解决 (准确率 ~92% vs 关键词 ~70%)

    与长期情感画像的关联 (参考 §4.2.2):
    ─────────────────────────────────────────────────────────────────
    每次会话的情绪检测结果会被记录到用户画像:
    · EmotionProfile.baseline_sentiment: 历史情绪基线 (识别"易怒型"客户)
    · EmotionProfile.sensitive_topics: 哪些话题触发负面情绪
    · EmotionProfile.churn_risk: 持续负面 → 标记高流失风险
    · EmotionProfile.preferred_tone: 据此调整AI回复语气

    Args:
        text: 用户输入文本 (来自当前轮次的用户消息)

    Returns:
        tuple[str, float]: (emotion_level, score)
        - emotion_level: "angry" | "negative" | "neutral"
        - score: 情绪强度分数, 范围 [0.1, 1.0], 越高越激烈
          · score > 0.85 通常触发 detect_escalation 的强制升级规则
    """
    # 统计愤怒关键词命中次数
    # 使用 sum + generator 而非 len + filter, 性能等价但更简洁
    angry_count = sum(1 for w in EMOTION_ANGRY_WORDS if w in text)
    # 统计负面关键词命中次数
    negative_count = sum(1 for w in EMOTION_NEGATIVE_WORDS if w in text)

    # ── 判定1: 愤怒 (最高优先级) ──
    # 触发条件: 2个以上愤怒词, 或 1个愤怒词 + 1个负面词的组合
    # 逻辑: 多个愤怒信号 = 确认愤怒; 愤怒+负面的组合 = 确认愤怒(交叉验证)
    if angry_count >= 2 or (angry_count >= 1 and negative_count >= 1):
        return "angry", min(0.5 + angry_count * 0.2 + negative_count * 0.1, 1.0)

    # ── 判定2: 明显负面 (含单个愤怒词的情况) ──
    # 触发条件: 2个以上负面词, 或仅1个愤怒词(无负面词配合)
    # 为何单个愤怒词归为 negative 而非 angry?
    #   → "投诉" 单独出现可能只是口头威胁/讨价还价策略, 不一定真的极端愤怒
    #   → 需要与其他信号(如多轮未解决、VIP)在升级决策中综合判定
    elif negative_count >= 2 or angry_count >= 1:
        return "negative", min(0.4 + negative_count * 0.15, 0.85)
        # 上限 0.85: 确保单独的 negative 级别不会触发 angry 的升级阈值(0.85)

    # ── 判定3: 轻度负面 ──
    # 触发条件: 仅有1个负面词
    # 含义: "这服务有点差" → 有不满但不严重, 安抚即可
    elif negative_count >= 1:
        return "negative", 0.3 + negative_count * 0.1
        # negative_count 此处必为 1, 所以 score = 0.4

    # ── 判定4: 中性 ──
    # 无任何负面/愤怒关键词命中, 用户情绪正常
    else:
        return "neutral", 0.1


