"""② 意图层 - 三级混合意图识别引擎 (Intent Recognition Engine)

本模块实现了智能客服系统架构中 **② 意图层** 的核心逻辑。
参考: docs/01-architecture-overview.md §3.3

┌─────────────────────────────────────────────────────────────────────────────┐
│ 设计哲学: "混合智能 + 够用即可"                                               │
│                                                                             │
│ 不是所有意图都需要大模型来识别。绝大多数客服场景(如"查物流"、"开发票")         │
│ 用简单的正则表达式就能以 0 成本、0 延迟准确命中。只有真正复杂/模糊的长尾        │
│ 查询才需要动用昂贵的 LLM。这就是"三级混合识别"的核心理念。                     │
│                                                                             │
│ 三级路由策略:                                                                 │
│   Level 1: 规则/正则匹配 (置信度 ≥ 0.9 直接命中)                              │
│            · 零成本、零延迟、零依赖                                            │
│            · 覆盖高确定性场景: "查物流"、"开发票"、"转人工"                     │
│            · 电商场景约 30% 的查询可被规则直接命中                              │
│                                                                             │
│   Level 2: 分类器 (本版为关键词加权, 生产环境应替换为 BERT 类小模型)           │
│            · 极低成本 (本地 CPU 推理, 5-20ms 延迟)                             │
│            · 覆盖常见意图的模糊表达                                            │
│            · 电商场景约 55% 的查询可被分类器覆盖                               │
│                                                                             │
│   Level 3: LLM 函数调用 (大模型兜底)                                          │
│            · 较高成本 (约 ¥0.05/次), 较高延迟 (400-2000ms)                     │
│            · 处理长尾、复合意图、模糊表达                                      │
│            · 仅约 15% 的查询需要走到此级                                       │
│                                                                             │
│ 成本效益 (参考 §3.6.1 月度成本模型):                                          │
│   路径A(规则命中): ¥0/次 × 30% = ¥0                                          │
│   路径B(分类器):   ¥0.002/次 × 55% = 极低成本                                │
│   路径C(LLM兜底): ¥0.05/次 × 15% = 可控成本                                 │
│                                                                             │
│ 架构位置:                                                                     │
│   输入层(①) → 【意图层(②)】 → 规划层(③) → 执行层(④) → 推理层(⑤)             │
│   意图层接收标准化后的文本输入, 输出结构化的 IntentResult,                      │
│   供规划层(LangGraph DAG)决定后续执行流程。                                    │
│                                                                             │
│ 本模块还包含:                                                                 │
│   · 情绪检测 (detect_emotion): 识别用户情绪状态                               │
│   · 升级决策引擎 (detect_escalation): 多信号融合判断是否需要转人工             │
│                                                                             │
│ 降级策略 (参考 §3.1 大促保护):                                                │
│   当系统负载等级升至 RED/BLACK 时, 跳过 Level 2/3, 仅走规则匹配,              │
│   未命中则返回通用意图, 保障系统可用性优先于识别精度。                          │
└─────────────────────────────────────────────────────────────────────────────┘
"""

