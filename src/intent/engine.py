class IntentEngine:
    """三级混合意图识别引擎 (Three-Level Hybrid Intent Recognition Engine)

    核心职责:
    ─────────────────────────────────────────────────────────────────
    1. 意图识别 (recognize): 三级瀑布式识别, 按成本从低到高逐级尝试
    2. 升级决策 (detect_escalation): 多信号融合判断是否需要转人工

    与架构的关联:
    ─────────────────────────────────────────────────────────────────
    · §3.3 ② 意图层: 本类实现了"混合识别(三级) + 二级槽位抽取"
    · §3.3 置信度路由: 高置信走规则/分类器; 低置信升级到 LLM; 不确定时澄清反问
    · §3.1 大促保护: system_level 控制降级行为
    · §4.3 人机协同: detect_escalation 实现转人工策略
    · §4.5.2 Trace: method 字段记录走了哪条路径

    系统负载降级策略 (参考 §3.1 + §3.6.1 规则R2):
    ─────────────────────────────────────────────────────────────────
    · GREEN: 全部三级可用, 正常运行
    · YELLOW: 全部三级可用, 但 LLM 可能被限流
    · RED: 仅 Level 1 (规则) 可用, Level 2/3 被禁止
    · BLACK: 仅 Level 1 (规则) 可用, 极端保护模式

    使用方式 (典型调用链):
    ─────────────────────────────────────────────────────────────────
    ```python
    # 初始化 (通常在服务启动时)
    engine = IntentEngine(config=intent_config, system_level=SystemLevel.GREEN)

    # 每次用户消息到达时调用
    intent_result = engine.recognize(session_context)
    should_escalate, reason = engine.detect_escalation(session_context)

    # 路由到规划层
    if should_escalate:
        route_to_human(reason)
    else:
        route_to_planner(intent_result)
    ```

    Attributes:
        config (IntentConfig): 意图层配置对象, 包含:
            - rule_confidence_threshold: 规则直接通过阈值 (默认 0.9)
            - classifier_confidence_threshold: 分类器通过阈值 (默认 0.8)
            - llm_fallback_enabled: LLM 兜底开关 (可通过配置中心热关闭)
        system_level (SystemLevel): 当前系统负载等级
            - 影响哪些级别可用
            - 由 SRE/自动化监控根据系统负载动态调整
    """

    def __init__(self, config: IntentConfig, system_level: SystemLevel = SystemLevel.GREEN):
        """初始化意图识别引擎

        Args:
            config: 意图层配置对象
                    包含各级阈值和开关, 运营可通过配置中心动态调整
                    (参考 §4.6 配置中心: "意图体系、流程图可按租户差异化下发")
            system_level: 系统负载等级, 默认 GREEN (全功能可用)
                          大促场景下 SRE 可手动/自动调整为 RED/BLACK
                          触发降级, 保障核心链路可用
                          (参考 §3.1 大促保护: "分级限流、降级")
        """
        self.config = config
        self.system_level = system_level

    def recognize(self, ctx: SessionContext) -> IntentResult:
        """执行三级意图识别 — 意图引擎的核心方法

        按照"成本从低到高、确定性从高到低"的原则, 依次尝试三个级别的识别方法。
        一旦某级识别结果的置信度达到该级的阈值, 立即返回, 不再尝试更高成本的级别。
        这就是"够用即可"设计原则的具体体现。

        三级瀑布式流程图:
        ─────────────────────────────────────────────────────────────────
        ┌───────────────────────────────────────────────────────────────┐
        │  用户文本 (ctx.last_user_message)                               │
        │       │                                                        │
        │       ▼                                                        │
        │  Level 1: 规则匹配 (rule_match)                                 │
        │  · 置信度 ≥ rule_confidence_threshold(0.9)?                     │
        │    · 是 → 直接返回 ✓  [零成本, <1ms]                            │
        │    · 否 → 继续 ↓                                               │
        │       │                                                        │
        │       ▼                                                        │
        │  [降级检查: system_level == RED/BLACK?]                          │
        │    · 是 → 返回 GENERAL_INQUIRY(conf=0.5, method="degraded") ✓   │
        │    · 否 → 继续 ↓                                               │
        │       │                                                        │
        │       ▼                                                        │
        │  Level 2: 分类器 (classifier_predict)                           │
        │  · 置信度 ≥ classifier_confidence_threshold(0.8)?               │
        │    · 是 → 合并规则槽位 → 返回 ✓  [极低成本, <1ms]               │
        │    · 否 → 继续 ↓                                               │
        │       │                                                        │
        │       ▼                                                        │
        │  Level 3: LLM 兜底 (仅 GREEN + 配置启用)                        │
        │  · llm_fallback_enabled 且 GREEN?                               │
        │    · 是 → 调用LLM → 返回 ✓  [¥0.05/次, 1-3s]                   │
        │    · 否 → 继续 ↓                                               │
        │       │                                                        │
        │       ▼                                                        │
        │  兜底: 返回分类器低置信结果 或 UNKNOWN(conf=0.3)                  │
        └───────────────────────────────────────────────────────────────┘

        降级行为详解 (参考 §3.1 + §6):
        ─────────────────────────────────────────────────────────────────
        RED/BLACK 模式下:
        · 跳过 Level 2 (分类器) 和 Level 3 (LLM)
        · 规则未命中 → 返回 GENERAL_INQUIRY (通用意图, confidence=0.5)
        · 规划层收到 GENERAL_INQUIRY → 走预设话术/FAQ兜底 → 可能建议转人工

        为什么 RED/BLACK 不保留分类器?
        · 分类器延迟极低(<1ms), 理论上可保留
        · 但极端降级追求"绝对确定性": 规则命中=可执行; 未命中=不冒险
        · 避免给后续流程增加无谓压力 (规划层/执行层可能也在降级)

        槽位合并策略:
        ─────────────────────────────────────────────────────────────────
        Level 2 分类器通过时, 检查 Level 1 是否有部分结果。
        即使 Level 1 整体置信度未达阈值, 它可能已抽取了有价值的槽位。
        例如: rule_match 返回 confidence=0.7 (不达标), 但抽到了 reason="质量"
        → 分类器给出最终意图判定, 同时保留规则抽取的槽位
        → 减少后续追问, 提升体验

        Args:
            ctx: 会话上下文对象, 包含:
                 · last_user_message: 用户最新消息文本 (已由输入层 §3.2 预处理)
                 · turn_count: 当前对话轮次 (用于 detect_escalation)
                 · is_vip: 是否VIP用户 (影响升级决策)
                 · status: 会话状态 (ACTIVE/RESOLVED/...)

        Returns:
            IntentResult: 意图识别结果, 必定返回一个结果 (不会返回 None)
            包含字段:
            · intent: 意图标签 (如 "REFUND"/"LOGISTICS"/"UNKNOWN")
            · confidence: 置信度 [0, 1] → 规划层据此决定是否追问
            · method: 识别方法 → 可观测性标记
              - "rule": Level 1 规则命中
              - "classifier": Level 2 分类器命中
              - "llm_fallback": Level 3 LLM 兜底
              - "degraded": 系统降级模式兜底
              - "none": 三级均无可靠结果
            · slots: 已抽取的槽位 (如 {"reason": "质量"})
            · sub_intents: 子意图列表 (仅 LLM 兜底时有, 用于多意图场景)
        """
        text = ctx.last_user_message or ""

        # ═══ Level 1: 规则匹配 ═══
        # 首先尝试成本最低、速度最快、确定性最高的规则引擎
        # 如果规则命中且置信度达标 (0.95 >= 0.9), 直接返回
        # 这是最理想路径: 零成本、零延迟、高确定性
        rule_result = rule_match(text)
        if rule_result and rule_result.confidence >= self.config.rule_confidence_threshold:
            # 规则置信度(0.95) >= 阈值(0.9) → 命中! 直接返回
            # 约 30% 的查询走这条路径 (参考月度成本模型)
            return rule_result

        # ═══ 降级模式检查: RED/BLACK 级别仅保留规则层 ═══
        # 参考 §3.1 大促保护: "分级限流(按租户/渠道/接口)、排队与降级"
        # 参考 §3.6.1 系统保护规则 R2:
        #   "system_level >= L3 → 禁止调用任何LLM, 走模板"
        # 在极端负载下, 系统可用性 > 识别精度
        if self.system_level in (SystemLevel.RED, SystemLevel.BLACK):
            # 降级策略: 规则未命中 → 返回通用意图
            # GENERAL_INQUIRY 语义: "我不确定用户想要什么"
            # 规划层收到此结果后的行为:
            #   → 使用预设话术回复 (如"很抱歉, 当前咨询量较大...")
            #   → 或建议用户转人工
            return IntentResult(
                intent="GENERAL_INQUIRY",
                confidence=0.5,   # 中等置信度, 表示不确定
                method="degraded",  # 标明降级, 用于监控告警
                # 当 method="degraded" 比例异常升高时:
                # → 触发 P2 告警 (参考 §4.5.3 告警规则体系)
                # → 可能说明降级时间过长, 需要评估是否恢复
            )

        # ═══ Level 2: 分类器 ═══
        # 规则未命中(或置信度不足 <0.9), 尝试分类器
        # 分类器能处理更灵活的表达方式 (不要求固定正则模式)
        cls_result = classifier_predict(text)
        if cls_result and cls_result.confidence >= self.config.classifier_confidence_threshold:
            # 分类器置信度达标 (>= 0.8) → 可信赖的结果

            # ── 槽位合并: 即使规则整体未达标, 其槽位仍有价值 ──
            # 场景: 规则匹配到了 REFUND + reason="质量" (conf=0.7, 未达0.9阈值)
            #       分类器也判定为 REFUND (conf=0.85, 达0.8阈值)
            #       → 最终结果用分类器的 conf, 但保留规则抽取的 reason 槽位
            if rule_result:
                cls_result.slots.update(rule_result.slots)
            return cls_result

        # ═══ Level 3: LLM 兜底 ═══
        # 规则和分类器都无法确定意图时, 使用大模型进行语义理解
        # 参考 §3.3: "长尾、复合、模糊表达由大模型做意图判定与工具选择, 兜底"
        # 条件: LLM 兜底功能已启用 + 系统处于正常状态 (GREEN)
        if self.config.llm_fallback_enabled and self.system_level == SystemLevel.GREEN:
            # ┌────────────────────────────────────────────────────────────┐
            # │ 【MVP 阶段: 模拟 LLM 返回】                                 │
            # │                                                            │
            # │ 生产环境应替换为真实的大模型 API 调用:                        │
            # │ · 使用 Function Calling 让 LLM 选择意图+工具+抽取参数        │
            # │ · 参考 §3.6.1 路由规则 R4:                                  │
            # │   "复杂推理/多意图/长上下文 → 大/超大模型(云端)"             │
            # │ · 超时处理: 3s 超时, 超时降级到分类器结果或模板话术           │
            # │ · 成本: 约 ¥0.05/次, 需纳入Token预算管理                    │
            # │   (参考 §3.6.1 Token预算管控体系)                           │
            # │ · 可处理: 复合意图("退款顺便开个发票")、                     │
            # │         模糊表达("上次那个问题还没解决")、                    │
            # │         长尾业务("活动规则太复杂了没看懂")                    │
            # │ · 参考 §3.3.1: 多话题同句由 LLM 拆解为多个 TopicFrame        │
            # └────────────────────────────────────────────────────────────┘
            return IntentResult(
                intent="COMPLEX_INQUIRY",     # 复合/复杂查询
                confidence=0.7,               # 模拟 LLM 给出的中等置信度
                method="llm_fallback",        # 标明走了 LLM 兜底路径
                sub_intents=["UNKNOWN"],      # LLM 可能拆解出多个子意图
                # 生产环境中 sub_intents 可能为:
                # ["REFUND", "INVOICE"] → 表示用户想退款+开发票
                # 参考 §3.3.1 话题管理: 依次入栈, 逐个处理
            )

        # ═══ 最终兜底 ═══
        # 所有级别都无法给出高置信结果时的兜底逻辑
        # 两种情况:
        #   1) 分类器有结果但置信度不够高 (0.3 <= conf < 0.8)
        #      → 返回分类器的低置信结果, 规划层据此触发"澄清式反问"
        #      → 参考 §3.3: "不确定时澄清式反问"
        #   2) 分类器也没有结果 (所有意图得分 < 0.3)
        #      → 返回 UNKNOWN, 规划层走默认话术
        return cls_result or IntentResult(
            intent="UNKNOWN",
            confidence=0.3,
            method="none",  # 三个级别都未能有效识别
            # 高频出现 method="none" 时, 应触发数据飞轮 (参考 §3.7):
            # "坏案例 → 标注 → 知识/Prompt/流程/分类器更新 → 灰度回归 → 上线"
            # 具体行动:
            #   1. 如果是新出现的固定表达 → 补充 INTENT_RULES
            #   2. 如果表达多样但意图明确 → 增加分类器训练数据
            #   3. 如果是复杂/模糊表达 → 优化 LLM prompt
        )

    def detect_escalation(self, ctx: SessionContext) -> tuple[bool, str]:
        """升级决策引擎 — 多信号融合判断是否需要转人工

        这不是简单的单一阈值判断, 而是综合考虑多个维度信号的加权决策引擎。
        实现了 §4.3 人机协同中定义的转人工策略:
        "用户主动请求、低置信、高风险动作、负面情绪升级、连续未解决"

        决策架构 (双层设计):
        ─────────────────────────────────────────────────────────────────

        ┌─── 第一层: 强制规则 (命中任一即升级, 无需打分) ───────────────┐
        │                                                             │
        │ 规则1: 用户明确要求转人工                                     │
        │   触发词: "转人工" / "真人" / "人工客服"                      │
        │   原因: 必须尊重用户意愿, 这是客服系统的基本契约              │
        │   reason: "user_requested"                                   │
        │                                                             │
        │ 规则2: 极端愤怒情绪                                           │
        │   条件: emotion == "angry" 且 emotion_score > 0.85            │
        │   原因: 用户已极度愤怒, AI继续服务只会加剧不满                │
        │   reason: "emotion_angry"                                    │
        │                                                             │
        │ 规则3: 多轮未解决                                             │
        │   条件: turn_count >= 5 且 status == ACTIVE (未解决)          │
        │   原因: 5轮仍未解决说明AI能力不足, 继续对话浪费用户时间       │
        │   reason: "unresolved_5_turns"                               │
        │   参考 §1.1: "端到端会话解决时长下降40%"目标                  │
        │                                                             │
        │ 规则4: VIP用户+任何负面情绪                                    │
        │   条件: is_vip == True 且 emotion ∈ {"negative", "angry"}    │
        │   原因: VIP流失成本极高, 任何不满都应快速由人工接管            │
        │   reason: "vip_negative"                                     │
        │   参考 §3.1: "优先放行VIP与资金类会话"                        │
        │   参考 §4.2.2: "高敏感客户 → 降低升级阈值"                    │
        │                                                             │
        └─────────────────────────────────────────────────────────────┘

        ┌─── 第二层: 加权评分 (综合多信号的软性决策) ───────────────────┐
        │                                                             │
        │ 当所有强制规则均未触发时, 通过加权公式计算综合分数:            │
        │                                                             │
        │ score =                                                      │
        │   emotion_score × 0.30      (情绪强度, 权重最高)             │
        │ + angry_words_hit × 0.25    (前4个愤怒关键词是否出现)         │
        │ + turn_ratio × 0.15         (对话轮次占比)                    │
        │ + vip_bonus × 0.10          (VIP额外加权)                    │
        │                                                             │
        │ 总权重 = 0.80 (预留 0.20 给未来新增维度, 如历史投诉次数等)    │
        │ 阈值: score >= 0.75 → 触发升级                               │
        │                                                             │
        │ 设计意图: 多个轻度信号叠加也可能触发升级                       │
        │ 例如: 中等情绪(0.6) + 含投诉词 + 4轮 + VIP → 可能达到0.75    │
        │                                                             │
        └─────────────────────────────────────────────────────────────┘

        为什么用多信号融合而不是单一阈值?
        ─────────────────────────────────────────────────────────────────
        · 单信号误判率高: 用户说"投诉"可能只是口头威胁/讨价还价策略
        · 多信号交叉验证: "投诉" + 5轮未解决 + VIP = 很可能确实需要升级
        · 灵活可调: 权重可通过配置中心按租户差异化调整
        · 可解释: reason 字段完整记录升级原因, 供质检和运营分析
        · 参考 §4.5.1 审计日志: action.type="ESCALATE" + reason

        Args:
            ctx: 会话上下文, 包含:
                 · last_user_message: 用户最新消息 (用于情绪检测和关键词匹配)
                 · turn_count: 当前对话轮次数 (越多=越可能需要升级)
                 · status: 会话状态 (ACTIVE=未解决, RESOLVED=已解决)
                 · is_vip: 是否VIP用户 (VIP用户降低升级阈值)

        Returns:
            tuple[bool, str]: (should_escalate, reason)
            - should_escalate: True=需要转人工, False=继续AI服务
            - reason: 升级原因代码 (用于审计日志 §4.5.1 和运营分析)
              可能的值:
              · "user_requested": 用户主动要求
              · "emotion_angry": 极端愤怒情绪
              · "unresolved_5_turns": 多轮未解决
              · "vip_negative": VIP用户+负面情绪
              · "weighted_score_X.XX": 加权评分达标
              · "": 空字符串 (不需要升级时)
        """
        text = ctx.last_user_message or ""

        # 先进行情绪检测, 作为后续决策的重要输入信号
        # 情绪检测结果同时会被记录到 Trace 中 (参考 §4.5.2)
        emotion, emotion_score = detect_emotion(text)

        # ═══ 强制升级规则1: 用户明确要求转人工 ═══
        # 最高优先级: 无论情绪如何、轮次多少, 用户说"转人工"必须立即响应
        # 参考 §4.3 转人工策略: "用户主动请求"排在首位
        # 这是客服系统与用户之间的基本契约: 用户有权随时获得人工服务
        if any(w in text for w in ["转人工", "真人", "人工客服"]):
            return True, "user_requested"

        # ═══ 强制升级规则2: 极端愤怒情绪 ═══
        # 当情绪检测为 angry 且强度超过 0.85 时, 强制升级
        # 0.85 阈值含义: 通常需要 2+ 个愤怒关键词 + 负面关键词同时出现
        # 示例: "你们是骗子! 我要投诉到消协去曝光!" → angry, score≈1.0
        # 此时继续AI对话只会火上浇油, 必须由有经验的人工客服接管
        if emotion == "angry" and emotion_score > 0.85:
            return True, "emotion_angry"

        # ═══ 强制升级规则3: 多轮未解决 ═══
        # 5轮仍处于 ACTIVE 状态(未解决), 说明AI无法处理当前场景
        # 继续对话只会浪费用户时间, 降低满意度
        # 5轮是经验阈值: 电商简单问题平均 2-3 轮解决
        # 超过5轮 = 明显超出AI能力边界
        # 参考 §1.1 业务目标: "端到端会话解决时长下降40%"
        if ctx.turn_count >= 5 and ctx.status == SessionStatus.ACTIVE:
            return True, "unresolved_5_turns"

        # ═══ 强制升级规则4: VIP用户 + 任何负面情绪 ═══
        # VIP用户的 LTV (终身价值) 远高于普通用户
        # 任何负面情绪都应快速由人工接管, 避免流失
        # 这是"宁可过度服务VIP, 也不冒流失风险"的策略
        # 参考 §4.2.2 画像驱动策略: "高敏感客户 → 降低升级阈值"
        # 参考 §3.1: "高峰期优先放行VIP与资金类会话"
        if ctx.is_vip and emotion in ("negative", "angry"):
            return True, "vip_negative"

        # ═══ 第二层: 加权评分 ═══
        # 强制规则均未触发时, 综合多个维度计算升级倾向分数
        # 多个轻度信号叠加也可能触发升级 (避免单信号遗漏)
        score = (
            # ── 信号1: 情绪强度 (权重 30%) ──
            # 情绪是最重要的单一信号: 情绪越激烈, 越需要升级
            # emotion_score 范围 [0.1, 1.0]
            # 贡献范围: 0.03 ~ 0.30
            emotion_score * 0.30

            # ── 信号2: 强愤怒关键词 (权重 25%) ──
            # EMOTION_ANGRY_WORDS[:4] = ["投诉", "消协", "曝光", "12315"]
            # 为什么只取前4个? → 这4个词代表"用户准备采取法律/媒体行动"
            # 后4个(赔偿/法院/报警/骂)虽然也是愤怒词, 但严重程度稍低
            # 任一命中 = 1.0, 无 = 0.0
            # 贡献: 0 或 0.25 (二值信号)
            + (1.0 if any(w in text for w in EMOTION_ANGRY_WORDS[:4]) else 0.0) * 0.25

            # ── 信号3: 对话轮次占比 (权重 15%) ──
            # 对话越长, 说明问题越难解决, 升级紧迫性越高
            # turn_count/5: 将轮次映射到 [0, 1.0] (5轮时为满分1.0)
            # min(..., 1.0): 超过5轮时已触发强制规则, 此处不再增加
            # 贡献范围: 0 ~ 0.15
            + min(ctx.turn_count / 5, 1.0) * 0.15

            # ── 信号4: VIP用户加权 (权重 10%) ──
            # VIP用户在边界case时倾向于转人工 (宁可过度服务)
            # is_vip → 固定贡献 0.2 × 0.10 = 0.02
            # 虽然数值小, 但在其他信号接近阈值时可能成为"压死骆驼的最后一根稻草"
            + (0.2 if ctx.is_vip else 0.0) * 0.10
        )

        # 综合分数达到 0.75 → 触发升级
        # 0.75 阈值设计偏保守(避免过度升级增加人工成本):
        #   · 仅情绪高(0.85×0.30=0.255) + 含投诉(0.25) = 0.505 < 0.75 (不升级)
        #   · 需要 3+ 个中等信号同时存在才能达到
        # 如果业务要求更积极的升级策略, 可降低此阈值 (通过配置中心调整)
        if score >= 0.75:
            return True, f"weighted_score_{score:.2f}"

        # 所有条件均未触发 → 不升级, 继续AI服务
        return False, ""


