# 分类器关键词权重表
# 结构: { 意图标签: [(关键词, 权重), ...] }
# 权重含义: 该关键词出现在文本中时, 对识别该意图的贡献度 (0-1)
# 设计考量: 当前权重值通过人工经验设定; 生产环境应通过标注数据统计/学习得到
CLASSIFIER_KEYWORDS: dict[str, list[tuple[str, float]]] = {
    # ── 退款类 ──
    # "退"字是核心信号(0.4), 与"款"/"钱"组合可提升置信度
    # "退款" 作为完整词给最高权重(0.6), 因为它几乎不会出现在非退款语境中
    "REFUND": [("退", 0.4), ("款", 0.3), ("钱", 0.3), ("退货", 0.5), ("退款", 0.6)],

    # ── 物流类 ──
    # "物流"和"快递"是强信号(0.5), 基本确定是物流查询
    # "到了"是弱信号(0.2): "到了吗"可能是物流, 但"到了哪个步骤"可能不是
    "LOGISTICS": [("物流", 0.5), ("快递", 0.5), ("配送", 0.4), ("到了", 0.2), ("发货", 0.4)],

    # ── 价保类 ──
    # 价保需要多信号共现: 单独的"价"(0.3)或"降"(0.3)置信度不足
    # "差价"(0.5)是较强信号, 但仍需搭配其他词(如"退差价"→"退"+0.4+"差价"+0.5=0.9)
    # 注意: 这可能与REFUND冲突, 但分类器取最高分, "差价"场景通常price_protect得分更高
    "PRICE_PROTECT": [("价", 0.3), ("降", 0.3), ("保", 0.3), ("差价", 0.5), ("便宜", 0.3)],

    # ── 投诉类 ──
    # "投诉"(0.6)是极强信号, 单独出现就超过0.3阈值
    # "服务差"(0.4)+"太差"(0.3)组合也能达到0.7的置信度
    "COMPLAINT": [("投诉", 0.6), ("服务差", 0.4), ("太差", 0.3), ("垃圾", 0.3)],

    # ── 发票类 ──
    # "发票"(0.6)是最强信号, 基本不会出现在非发票语境中
    # "抬头"(0.4)辅助确认: "修改抬头" → 发票+抬头=1.0
    "INVOICE": [("发票", 0.6), ("税", 0.3), ("抬头", 0.4)],

    # ── 导购推荐类 ──
    # "推荐"(0.6)是强信号；"哪款/哪个"(0.3)、"性价比/值得买"辅助
    "RECOMMEND": [("推荐", 0.6), ("哪款", 0.3), ("哪个好", 0.4),
                  ("性价比", 0.3), ("值得买", 0.4), ("买什么", 0.4)],

    # ── FAQ类 (通用信息查询) ──
    # 疑问词是弱信号(0.2): "怎么退款" 中 "怎么" 对 FAQ 贡献 0.2, "退" 对 REFUND 贡献 0.4
    # → REFUND 得分更高, FAQ 不会抢占 → 设计合理
    # 只有纯信息查询("规则是什么"/"政策有哪些")时 FAQ 才能获得较高分
    "FAQ": [("怎么", 0.2), ("如何", 0.2), ("什么", 0.2), ("规则", 0.3), ("政策", 0.3)],
}


def classifier_predict(text: str) -> Optional[IntentResult]:
    """Level 2: 轻量分类器 — 基于关键词加权的意图分类

    当 Level 1 规则匹配失败时, 使用本分类器进行意图识别。
    本实现为 MVP 阶段的简化版本, 生产环境应替换为 BERT fine-tuned 模型。

    算法原理:
    ─────────────────────────────────────────────────────────────────
    1. 对每个候选意图, 扫描文本中是否包含该意图的关键词
    2. 将命中关键词的权重累加作为该意图的置信度分数
    3. 所有分数截断到 [0, 1.0] 范围 (防溢出)
    4. 取最高分的意图作为预测结果
    5. 最高分低于阈值(0.3)时认为不可靠, 返回 None

    与 Level 1 的区别:
    ─────────────────────────────────────────────────────────────────
    · Level 1 是"精确匹配": 一条规则命中就直接返回, 不考虑其他可能性
    · Level 2 是"打分比较": 所有意图都参与打分, 取最高分者
    · Level 2 能处理更灵活的表达 (不需要固定的正则模式)
    · 例如: "我的快递发了吗, 怎么还没收到啊"
      → "快递"(0.5) + "发"(0.4) + "收到"(0 - 未配置) = LOGISTICS 得 0.9

    阈值设计 (0.3):
    ─────────────────────────────────────────────────────────────────
    · 低于 0.3: 仅有 1 个弱信号关键词命中, 置信度不足
    · 典型场景: 用户说"怎么回事" → FAQ 得分 0.2 (仅"怎么"命中) → 不可靠
    · 这类模糊查询将被交给 Level 3 (LLM) 处理
    · 参考 §3.3: "不确定时澄清式反问"

    上限截断设计 (min(score, 1.0)):
    ─────────────────────────────────────────────────────────────────
    · 防止多关键词堆叠导致分数超过 1.0
    · 例如: "退款退货退钱" → "退"(0.4)+"款"(0.3)+"钱"(0.3)+"退货"(0.5)+"退款"(0.6) = 2.1
      → 截断为 1.0
    · 确保分数语义统一: 所有 confidence 都在 [0, 1] 范围内

    边界情况:
    ─────────────────────────────────────────────────────────────────
    · 空文本: 所有意图得分为 0 → best_score < 0.3 → 返回 None
    · 多意图同分: max() 返回字典迭代顺序中的第一个 (Python 3.7+ dict有序)
      → 生产环境应加入消歧逻辑或交给 LLM 处理多意图
      → 参考 §3.3.1 话题管理: "多话题同句"由 LLM 拆解
    · 关键词重叠: "退货条件" → REFUND("退货"=0.5, "退"=0.4) vs FAQ("规则"=0)
      → REFUND 得分更高, 能正确区分

    性能特征:
    ─────────────────────────────────────────────────────────────────
    · 时间复杂度: O(I × K), I=意图数(6), K=平均关键词数(~5) ≈ 30次字符串包含判断
    · 延迟: < 0.5ms (纯字符串操作)
    · 成本: ¥0 (无外部调用)
    · 对比 BERT: 延迟 5-20ms, 但准确率从 ~70% 提升到 ~92%

    Args:
        text: 用户输入文本 (来自输入层预处理后的标准化文本)

    Returns:
        IntentResult: 分类成功时返回 (confidence >= 0.3, method="classifier")
        None: 最高分低于 0.3 阈值, 不可靠, 交由 Level 3 处理
    """
    scores: dict[str, float] = {}

    for intent, keywords in CLASSIFIER_KEYWORDS.items():
        score = 0.0
        for kw, weight in keywords:
            # 使用 Python `in` 操作符进行子串包含判断
            # 注意: 这是简化实现, 不考虑词边界
            # 例如: 关键词"退" 会匹配 "退货"、"后退"、"退一步" 等所有含"退"字的文本
            # 生产环境的 BERT 分类器基于语义向量, 不存在此问题
            if kw in text:
                score += weight
        # 截断到 [0, 1.0] 范围, 确保置信度语义一致性
        scores[intent] = min(score, 1.0)

    # 防御性编程: 如果 CLASSIFIER_KEYWORDS 为空(理论上不应发生)
    if not scores:
        return None

    # 取得分最高的意图作为预测结果
    best_intent = max(scores, key=scores.get)
    best_score = scores[best_intent]

    # 置信度阈值检查: 低于 0.3 认为不可靠, 不返回结果
    # 语义: 最多只有一个弱信号关键词命中 (如"什么"→FAQ=0.2), 无法做可靠判定
    # 这类查询将交给 Level 3 (LLM) 进行语义理解
    if best_score < 0.3:
        return None

    return IntentResult(
        intent=best_intent,
        confidence=best_score,
        method="classifier",  # 标明来源为分类器, 区别于 "rule" 和 "llm_fallback"
        # 注意: 分类器级别不做槽位抽取
        # 槽位抽取策略: 规则层已抽取的槽位会在 recognize() 中合并过来
        # 缺失的槽位由规划层触发追问流程获取 (参考 §3.3 缺失槽位追问策略)
    )


